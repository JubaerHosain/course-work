# Convert categorical features to numeric labels
def encode_labels(data):
    label_dict = {}
    le = LabelEncoder()
    for key in data.keys():
        data[key] = le.fit_transform(data[key])
        label_dict[key] = dict(zip(le.classes_, le.transform(le.classes_)))
        
    return label_dict
        

